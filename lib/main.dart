import 'package:flutter/foundation.dart' show kIsWeb;


void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  
    // データベースの初期化【Webはスキップ】
  if (!kIsWeb) {
    try {
      await DatabaseService.instance.database;
    } catch (e) {
      // Webではデータベース初期化エラーを無視
    }
  }
  
  runApp(const TruckLogApp());
}

